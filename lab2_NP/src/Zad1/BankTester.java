class Account {
    private static final Random RANDOM = new Random();
    private long id;
    private String name;
    private double balance;

    public Account(String name, double balance) {
        this.id = RANDOM.nextLong(); // генерирање уникатен идентификационен број
        this.name = name;
        this.balance = balance;
    }

    public double getBalance() {
        return balance;
    }

    public String getName() {
        return name;
    }

    public long getId() {
        return id;
    }

    public void setBalance(double balance) {
        this.balance = balance;
    }

    @Override
    public String toString() {
        return String.format("Name: %s\nBalance: %.2f$\n", name, balance);
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Account account = (Account) o;
        return id == account.id && Double.compare(balance, account.balance) == 0 && Objects.equals(name, account.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id, name, balance);
    }
}

abstract class Transaction {
    private final long fromId;
    private final long toId;
    private final String description;
    private final double amount;

    public Transaction(long fromId, long toId, String description, double amount) {
        this.fromId = fromId;
        this.toId = toId;
        this.description = description;
        this.amount = amount;
    }

    public double getAmount() {
        return amount;
    }

    public long getFromId() {
        return fromId;
    }

    public long getToId() {
        return toId;
    }

    public String getDescription() {
        return description;
    }

    public abstract double getProvision();

    @Override
    public boolean equals(Object obj) {
        if (this == obj) return true;
        if (obj == null || getClass() != obj.getClass()) return false;
        Transaction that = (Transaction) obj;
        return fromId == that.fromId && toId == that.toId && Double.compare(that.amount, amount) == 0;
    }

    @Override
    public int hashCode() {
        return Objects.hash(fromId, toId, amount);
    }
}

class FlatAmountProvisionTransaction extends Transaction {
    private final double flatProvision;

    public FlatAmountProvisionTransaction(long fromId, long toId, double amount, double flatProvision) {
        super(fromId, toId, "FlatAmount", amount);
        this.flatProvision = flatProvision;
    }

    @Override
    public double getProvision() {
        return flatProvision;
    }

    public double getFlatAmount() {
        return flatProvision;
    }

    @Override
    public boolean equals(Object obj) {
        if (!super.equals(obj)) return false;
        FlatAmountProvisionTransaction that = (FlatAmountProvisionTransaction) obj;
        return Double.compare(that.flatProvision, flatProvision) == 0;
    }

    @Override
    public int hashCode() {
        return Objects.hash(super.hashCode(), flatProvision);
    }
}

class FlatPercentProvisionTransaction extends Transaction {
    private final int percent;

    public FlatPercentProvisionTransaction(long fromId, long toId, double amount, int percent) {
        super(fromId, toId, "FlatPercent", amount);
        this.percent = percent;
    }

    @Override
    public double getProvision() {
        return (getAmount() * percent) / 100.0;
    }

    public int getPercent() {
        return percent;
    }

    @Override
    public boolean equals(Object obj) {
        if (!super.equals(obj)) return false;
        FlatPercentProvisionTransaction that = (FlatPercentProvisionTransaction) obj;
        return percent == that.percent;
    }

    @Override
    public int hashCode() {
        return Objects.hash(super.hashCode(), percent);
    }
}


class Bank {
    private String name;
    private List<Account> accounts;
    private double totalTransfers;
    private double totalProvision;

    public Bank(String name, Account[] accountsArray) {
        this.name = name;
        this.accounts = new ArrayList<>();
        for (Account account : accountsArray) {
            this.accounts.add(new Account(account.getName(), account.getBalance()));
        }
    }

    public boolean makeTransaction(Transaction t) {
        Account fromAccount = findAccountById(t.getFromId());
        Account toAccount = findAccountById(t.getToId());

        if (fromAccount == null || toAccount == null || fromAccount.getBalance() < (t.getAmount() + t.getProvision())) {
            return false;
        }

        fromAccount.setBalance(fromAccount.getBalance() - (t.getAmount() + t.getProvision()));
        toAccount.setBalance(toAccount.getBalance() + t.getAmount());

        totalTransfers += t.getAmount();
        totalProvision += t.getProvision();
        return true;
    }

    private Account findAccountById(long id) {
        return accounts.stream().filter(account -> account.getId() == id).findFirst().orElse(null);
    }

    public double totalTransfers() {
        return totalTransfers;
    }

    public double totalProvision() {
        return totalProvision;
    }

    @Override
    public String toString() {
        StringBuilder sb = new StringBuilder();
        sb.append(String.format("Name: %s\n\n", name));
        for (Account account : accounts) {
            sb.append(account.toString());
        }
        return sb.toString();
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) return true;
        if (obj == null || getClass() != obj.getClass()) return false;
        Bank bank = (Bank) obj;
        return name.equals(bank.name);
    }

    @Override
    public int hashCode() {
        return Objects.hash(name, accounts);
    }


    public Account[] getAccounts() {
        return accounts.toArray(new Account[0]);
    }
}

